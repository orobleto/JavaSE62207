package com.educacionit.clase01;

public class OperadoresAsignacion2 {
	public static void main(String[] args) {
		int contador = 0;
		System.out.println(contador);
		contador++;
		System.out.println(contador);
		++contador;
		System.out.println(contador);
		System.out.println("**********");
		System.out.println(contador++);
		
		System.out.println(contador);
		
		System.out.println(++contador);
	}
}
