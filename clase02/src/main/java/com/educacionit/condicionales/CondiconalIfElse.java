package com.educacionit.condicionales;

public class CondiconalIfElse {
	public static void main(String[] args) {
		int num1 = 2, num2 = 3;
		char operador = '-';

		// suma y resta

		if (operador == '+') {
			System.out.println(num1 + num2);
		} else {
			System.out.println(num1 - num2);
		}

	}
}
